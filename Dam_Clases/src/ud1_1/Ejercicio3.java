    public static void main(String[] args) {
        // Ejercicio 3
        double radio = 5.2; // Decralamos el radio
        final double PI = 3.14; // Declaracion del valor de PI tipo constante
        double area; // Declaramos la variable para guardar el resultado

        area = PI * (radio * radio); // Calculamos el area segun  la formula // area = PI*r^2

        // Mostramos por pantalla el resultado
        System.out.println("El area de la circunferencia es " + area + "metros. ");
    }
