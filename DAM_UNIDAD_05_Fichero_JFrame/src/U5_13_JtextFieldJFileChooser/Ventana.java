    private void rutaBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rutaBtActionPerformed
        //Creamo una instancia de JFileChooser
        JFileChooser fc=new JFileChooser();

        //Escribimos el nombre del titulo
        fc.setDialogTitle("Elige un fichero");
        
        //Indicamos que solo se puedan elegir ficheros
        fc.setFileSelectionMode(JFileChooser.FILES_ONLY);
        
        //Creamos un filtro para JFileChooser
        FileNameExtensionFilter filtro = new  FileNameExtensionFilter("*.txt", "txt");
        fc.setFileFilter(filtro);
        
        int eleccion=fc.showSaveDialog(this);
        if (eleccion==JFileChooser.APPROVE_OPTION) {
            rutaTf.setText(fc.getSelectedFile().getPath()); 
        }
    }//GEN-LAST:event_rutaBtActionPerformed

